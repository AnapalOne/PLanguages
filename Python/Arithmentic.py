# NavigateFQArithmentic

# Intro
print("This program will show the product, quotient, sum, difference,\nremainder, exponent, and floor division of the \ntwo numbers you have given.")

# Number Input
num1 = int(input("Please input your first number:")) 
num2 = int(input("Please input your second number:"))

# Operations
product = num1 * num2
quotient = num1 / num2
sum = num1 + num2
difference = num1 - num2
remainder = num1 % num2
exponent = num1 ** num2
floordiv = num1 // num2

# Operation Results
print("===================\nResults...\n-----------------------------")
print("Product:",product,"\nQuotient:",quotient,"\nSum:",sum,"\nDifference:",difference,"\nRemainder:",remainder,"\nExponent:",exponent,"\nFloorDivision:",floordiv)
      
